/* Check if a number is positive and even */
let num:number=7
if(num>0){
    if(num%2==0){
        console.log(`The number ${num} is a Positive number and a Even number`)
    }else{
        console.log(`The number ${num} is a Positive number but not a Even number`)
    }
}else{
    console.log(`The number ${num} is not a Positive number`)
}